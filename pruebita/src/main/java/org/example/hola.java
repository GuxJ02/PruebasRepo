package org.example;

public class hola {
    String hh;
    int hhs;

    public hola(String hh, int hhs) {
        this.hh = hh;
        this.hhs = hhs;
    }

    public String getHh() {
        return hh;
    }

    public void setHh(String hh) {
        this.hh = hh;
    }

    public int getHhs() {
        return hhs;
    }

    public void setHhs(int hhs) {
        this.hhs = hhs;
    }
}
